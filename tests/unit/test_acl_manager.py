def test_render_all_uses_strict_undefined_base(
    registry: RegionRegistry,
    mock_docker_client: MagicMock,
    tmp_path: Path,
) -> None:
    """Base template referencing an unknown variable must raise GliaError."""
    tmpl_dir = tmp_path / "templates"
    tmpl_dir.mkdir()
    # Base uses a variable that isn't passed (`mystery`).
    (tmpl_dir / "_base.j2").write_text(
        "user {{ region }}\n# unknown: {{ mystery }}\n"
    )
    (tmpl_dir / "amygdala.j2").write_text("topic read foo\n")

    mgr = AclManager(
        registry,
        docker_client=mock_docker_client,
        template_dir=tmpl_dir,
        bus_dir=tmp_path,
        reload_sleep_s=0.0,
    )
    with pytest.raises(GliaError):
        mgr.render_all(regions=["amygdala"])


def test_render_all_missing_region_template_raises_gliaerror(
    registry: RegionRegistry,
    mock_docker_client: MagicMock,
    tmp_path: Path,
) -> None:
    tmpl_dir = tmp_path / "templates"
    tmpl_dir.mkdir()
    (tmpl_dir / "_base.j2").write_text("user {{ region }}\n")
    # no amygdala.j2 created on purpose

    mgr = AclManager(
        registry,
        docker_client=mock_docker_client,
        template_dir=tmpl_dir,
        bus_dir=tmp_path,
        reload_sleep_s=0.0,
    )
    with pytest.raises(GliaError):
        mgr.render_all(regions=["amygdala"])


