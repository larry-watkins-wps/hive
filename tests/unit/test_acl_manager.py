@pytest.mark.asyncio
async def test_reload_broker_sends_sighup(
    acl_manager: AclManager, mock_docker_client: MagicMock
) -> None:
    await acl_manager.reload_broker()
    mock_docker_client.containers.get.assert_called_with("mosquitto")
    mock_docker_client.containers.get.return_value.kill.assert_called_once_with(
        signal="SIGHUP"
    )


@pytest.mark.asyncio
async def test_reload_broker_container_not_found_raises_gliaerror(
    registry: RegionRegistry,
    tmp_path: Path,
) -> None:
    client = MagicMock()
    client.containers.get.side_effect = NotFound("no such container: mosquitto")
    mgr = AclManager(
        registry,
        docker_client=client,
        template_dir=REAL_TEMPLATE_DIR,
        bus_dir=tmp_path,
        reload_sleep_s=0.0,
    )
    with pytest.raises(GliaError):
        await mgr.reload_broker()


@pytest.mark.asyncio
async def test_reload_broker_api_error_wrapped(
    registry: RegionRegistry,
    tmp_path: Path,
) -> None:
    client = MagicMock()
