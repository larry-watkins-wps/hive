@pytest.mark.asyncio
async def test_render_and_apply_writes_atomic(
    acl_manager: AclManager,
    mock_docker_client: MagicMock,
    tmp_path: Path,
) -> None:
    result = await acl_manager.render_and_apply()

    assert isinstance(result, ApplyResult)
    assert result.ok is True
    assert result.reason is None

    acl_file = tmp_path / "acl.conf"
    tmp_file = tmp_path / "acl.conf.tmp"
    assert acl_file.exists(), "acl.conf must be written"
    assert not tmp_file.exists(), "acl.conf.tmp must have been renamed away"
    assert result.path == acl_file

    content = acl_file.read_text(encoding="utf-8")
    assert "user amygdala" in content
    assert "user prefrontal_cortex" in content

    # SIGHUP sent to mosquitto container
    mock_docker_client.containers.get.assert_called_with("mosquitto")
    mock_docker_client.containers.get.return_value.kill.assert_called_once_with(
        signal="SIGHUP"
    )


@pytest.mark.asyncio
async def test_render_and_apply_conflict_short_circuits(
    acl_manager: AclManager,
    mock_docker_client: MagicMock,
    tmp_path: Path,
    monkeypatch: pytest.MonkeyPatch,
) -> None:
    def fake_check(_self: AclManager, per_region: list[str]) -> str | None:
        return "dup topic-write claim"

    monkeypatch.setattr(AclManager, "_check_conflicts", fake_check)

    result = await acl_manager.render_and_apply()

    assert result.ok is False
    assert result.path is None
    assert result.reason is not None and "dup" in result.reason

    # No write, no broker reload.
    assert not (tmp_path / "acl.conf").exists()
    mock_docker_client.containers.get.return_value.kill.assert_not_called()


