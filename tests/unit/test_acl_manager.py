def test_render_all_includes_user_directives_for_each_active_region(
    acl_manager: AclManager,
) -> None:
    rendered = acl_manager.render_all()
    for name in ACTIVE_REGIONS:
        assert f"user {name}" in rendered, (
            f"missing `user {name}` directive in rendered acl.conf"
        )


def test_render_all_inlines_base_rules_per_region(
    acl_manager: AclManager,
) -> None:
    """Every region section contains the characteristic base pattern rules."""
    rendered = acl_manager.render_all()
    # pattern rules from _base.j2 are global — expect exactly one occurrence
    # in the canonical output when written literally (%c is mosquitto's
    # per-connection placeholder).
    assert rendered.count("pattern write hive/system/heartbeat/%c") == len(
        ACTIVE_REGIONS
    )
    assert rendered.count("pattern write hive/system/region_stats/%c") == len(
        ACTIVE_REGIONS
    )
    assert rendered.count("topic read hive/broadcast/#") == len(ACTIVE_REGIONS)


def test_render_all_inlines_region_specific_rules(
    acl_manager: AclManager,
) -> None:
    rendered = acl_manager.render_all()
    # amygdala's per-region rule
    assert "topic write hive/modulator/cortisol" in rendered
    # hippocampus-specific rule from hippocampus.j2 (amygdala writes this)
    assert "topic write hive/cognitive/hippocampus/encoding_hint" in rendered
    # a sampling from visual_cortex
    assert "hive/sensory/visual" in rendered


def test_render_all_generated_header_present(acl_manager: AclManager) -> None:
    rendered = acl_manager.render_all()
    assert rendered.startswith("# Generated by glia acl_manager")
    assert "DO NOT EDIT" in rendered.splitlines()[0]


def test_render_all_excludes_reserved_regions(acl_manager: AclManager) -> None:
    rendered = acl_manager.render_all()
    for reserved in RESERVED_REGIONS:
        assert f"user {reserved}" not in rendered, (
            f"reserved region {reserved} leaked into acl.conf"
        )


def test_render_all_order_stable(acl_manager: AclManager) -> None:
    """Two successive renders must produce byte-identical output."""
    a = acl_manager.render_all()
    b = acl_manager.render_all()
    assert a == b


def test_render_all_explicit_region_list(acl_manager: AclManager) -> None:
    """When passed a specific region list, only those regions appear."""
    rendered = acl_manager.render_all(regions=["amygdala", "hippocampus"])
    assert "user amygdala" in rendered
    assert "user hippocampus" in rendered
    assert "user motor_cortex" not in rendered
    assert "user prefrontal_cortex" not in rendered


