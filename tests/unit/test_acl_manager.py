"""Unit tests for glia.acl_manager — Task 5.5.

Covers:
  * render_all produces a concatenated acl.conf with per-region sections,
    each prefixed by a `user <name>` directive from _base.j2 and followed
    by the region-specific rules.
  * Reserved regions (raphe_nuclei, locus_coeruleus, hypothalamus,
    basal_forebrain, cerebellum) are excluded.
  * Strict-undefined: a missing variable in a template surfaces as
    a GliaError.
  * Missing per-region template -> GliaError.
  * render_and_apply performs atomic write (tmp -> rename) and sends
    SIGHUP to the mosquitto container.
  * reload_broker error paths (NotFound, APIError) wrap into GliaError.
"""
from __future__ import annotations

from pathlib import Path
from unittest.mock import MagicMock

